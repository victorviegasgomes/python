valor1 = int(input("digite um valor: "))
valor2 = int(input("digite outro valor: "))
print('soma = {}'.format(valor1+valor2))
print('subtração = {}'.format(valor1-valor2))
print('multiplicação = {}'.format(valor1*valor2))
print('divisão = {}'.format(valor1/valor2))
print('divisão inteira = {}'.format(valor1//valor2))
print('módulo = {}'.format(valor1%valor2))
print('potencia = {}'.format(valor1**valor2))
